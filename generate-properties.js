/**
 * PROPERTY DATA GENERATOR WITH CATEGORIES
 * ========================================
 * Run this script to auto-generate properties.js from your folder structure
 * 
 * Usage: node generate-properties.js
 */

const fs = require('fs');
const path = require('path');

const PROJECTS_DIR = './static/images/projects';
const OUTPUT_FILE = './static/js/properties.js';

// Define your categories
const CATEGORIES = [
    'house-and-lot',
    'lot-only',
    'commercial-space',
    'commercial-lot'
];

/**
 * Scan all categories and their projects
 */
function scanProjects() {
    const projectsByCategory = {};
    let totalProjects = 0;

    // Check if directory exists
    if (!fs.existsSync(PROJECTS_DIR)) {
        console.error(`‚ùå Directory not found: ${PROJECTS_DIR}`);
        console.log('üí° Make sure you run this from the root of your project');
        process.exit(1);
    }

    console.log(`üîç Scanning categories...\n`);

    CATEGORIES.forEach(category => {
        const categoryPath = path.join(PROJECTS_DIR, category);
        
        // Skip if category folder doesn't exist
        if (!fs.existsSync(categoryPath)) {
            console.log(`‚ö†Ô∏è  Category folder not found: ${category} (skipping)`);
            projectsByCategory[category] = {};
            return;
        }

        const folders = fs.readdirSync(categoryPath, { withFileTypes: true })
            .filter(dirent => dirent.isDirectory())
            .map(dirent => dirent.name);

        console.log(`üìÅ ${category}: ${folders.length} project(s)`);

        projectsByCategory[category] = {};

        folders.forEach(folder => {
            const folderPath = path.join(categoryPath, folder);
            const files = fs.readdirSync(folderPath);

            // Get all image files (excluding description file)
            const images = files.filter(file =>
                /\.(jpg|jpeg|png|gif|webp)$/i.test(file) &&
                !file.startsWith('zzz_')
            ).sort(); // Sort alphabetically

            // Use folder name directly as project name
            let projectName = folder;

            projectsByCategory[category][folder] = {
                name: projectName,
                location: "Calamba, Laguna",
                category: category,
                folder: folder,
                images: images
            };

            console.log(`  ‚úì ${projectName}: ${images.length} image${images.length !== 1 ? 's' : ''}`);
            totalProjects++;
        });

        console.log('');
    });

    return { projectsByCategory, totalProjects };
}

/**
 * Format category name for display
 */
function formatCategoryName(category) {
    return category
        .split('-')
        .map(word => word.charAt(0).toUpperCase() + word.slice(1))
        .join(' ');
}

/**
 * Generate the properties.js file
 */
function generateFile() {
    console.log('üî® Generating properties.js...\n');

    const { projectsByCategory, totalProjects } = scanProjects();

    // Format the projects object with proper indentation
    const projectsJson = JSON.stringify(projectsByCategory, null, 8)
        .replace(/"([^"]+)":/g, '"$1":');

    // Generate category metadata
    const categoriesMetadata = CATEGORIES.map(cat => ({
        id: cat,
        name: formatCategoryName(cat)
    }));

    const fileContent = `/**
 * AUTO-GENERATED PROPERTY CONFIG WITH CATEGORIES
 * ==============================================
 * ‚ö†Ô∏è  DO NOT EDIT THIS FILE MANUALLY
 * 
 * Generated: ${new Date().toISOString()}
 * Generator: generate-properties.js
 * 
 * To regenerate: Run 'node generate-properties.js'
 * 
 * WARNING: Manual edits will be overwritten!
 */

window.PROPERTY_DATA = {
    // Base path - DON'T CHANGE THIS
    basePath: "/static/images/projects/",
    
    // ===================================
    // CATEGORIES
    // ===================================
    categories: ${JSON.stringify(categoriesMetadata, null, 4)},
    
    // ===================================
    // AUTO-GENERATED PROJECTS BY CATEGORY
    // ===================================
    projectsByCategory: ${projectsJson},
    
    // ===================================
    // AUTOMATIC FUNCTIONS - DON'T EDIT
    // ===================================
    
    /**
     * Get property with full image paths
     */
    async getProperty(category, id) {
        const project = this.projectsByCategory[category]?.[id];
        if (!project) return null;
        
        // Build full image paths
        const images = project.images.map(filename => 
            this.basePath + category + "/" + project.folder + "/" + filename
        );
        
        // Try to load description
        let description = null;
        try {
            const response = await fetch(this.basePath + category + "/" + project.folder + "/zzz_description.txt");
            if (response.ok) {
                description = await response.text();
            }
        } catch (error) {
            // No description file, that's okay
        }
        
        return {
            id: id,
            category: category,
            categoryName: this.getCategoryName(category),
            name: project.name,
            location: project.location,
            folder: project.folder,
            images: images,
            description: description,
            features: [
                { icon: "fas fa-home", text: "Residential Property" },
                { icon: "fas fa-map-marker-alt", text: project.location },
                { icon: "fas fa-tag", text: this.getCategoryName(category) },
                { icon: "fas fa-images", text: images.length + " Photo" + (images.length !== 1 ? "s" : "") }
            ]
        };
    },
    
    /**
     * Get all properties in a category
     */
    async getPropertiesByCategory(category) {
        const projects = this.projectsByCategory[category] || {};
        const all = {};
        for (let id in projects) {
            all[id] = await this.getProperty(category, id);
        }
        return all;
    },
    
    /**
     * Get all properties across all categories
     */
    async getAllProperties() {
        const all = {};
        for (let category in this.projectsByCategory) {
            all[category] = await this.getPropertiesByCategory(category);
        }
        return all;
    },
    
    /**
     * Get category display name
     */
    getCategoryName(categoryId) {
        const category = this.categories.find(cat => cat.id === categoryId);
        return category ? category.name : categoryId;
    },
    
    /**
     * Get total project count
     */
    getTotalProjects() {
        let count = 0;
        for (let category in this.projectsByCategory) {
            count += Object.keys(this.projectsByCategory[category]).length;
        }
        return count;
    }
};

// Backwards compatibility
window.PROPERTY_CONFIG = window.PROPERTY_DATA;
window.PROPERTY_LOADER = window.PROPERTY_DATA;

/**
 * =============================================
 * HOW TO ADD NEW PROJECTS WITH CATEGORIES
 * =============================================
 * 
 * Step 1: Create category folder and project folder
 * --------------------------------------------------
 * /static/images/projects/house-and-lot/property-1/
 *   ‚îú‚îÄ‚îÄ photo1.jpg
 *   ‚îú‚îÄ‚îÄ photo2.jpg
 *   ‚îú‚îÄ‚îÄ photo3.jpg
 *   ‚îî‚îÄ‚îÄ zzz_description.txt  (optional)
 * 
 * Available categories:
 * - house-and-lot
 * - lot-only
 * - commercial-space
 * - commercial-lot
 * 
 * Step 2: Run the generator
 * -------------------------
 * node generate-properties.js
 * 
 * Step 3: Upload & Done!
 * ----------------------
 * Git will commit the updated properties.js
 * Visit: yoursite.com/property.html?category=house-and-lot&id=property-1
 * 
 * =============================================
 */
`;

    // Ensure output directory exists
    const outputDir = path.dirname(OUTPUT_FILE);
    if (!fs.existsSync(outputDir)) {
        fs.mkdirSync(outputDir, { recursive: true });
    }

    fs.writeFileSync(OUTPUT_FILE, fileContent);

    console.log(`\n‚úÖ Generated ${OUTPUT_FILE}`);
    console.log(`üì¶ Total projects: ${totalProjects}`);
    console.log(`üìÇ Categories: ${CATEGORIES.length}`);
    console.log(`\nüöÄ Ready to commit and push!`);
}

// Run the generator
try {
    generateFile();
} catch (error) {
    console.error('‚ùå Error:', error.message);
    process.exit(1);
}
